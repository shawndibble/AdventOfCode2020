function getCount($right, $down, $rows) {
  $x = 0;
  $trees = 0;
  $skip = $down;

  // skip the first row as we don't have any $down == 0;
  array_shift($rows);
  
  
  foreach($rows as $row) {
    
    // decrement our skip (based on $down). If it is 0, we should process this row. Otherwise skip it.
    $skip--;
    if ($skip != 0) {
      continue;
    }
    // set the new x offset based on the last x
    $x = $x + $right;
    
    // if our row length is less than the offset, then start repeating the string.
    $length = strlen($row);
    if ($x+1 > $length) {
      $row = str_repeat($row, ceil($x/($length-1)));
    }

    // Grab the . or # from the new extended row. Increment our counter if it is an #.
    $entry = $row[$x];
    if ($entry === '#') {
      $trees++;
    }
    
    //Since we didn't skip this row, reset it.
    $skip = $down;
  }
  return $trees;
}

